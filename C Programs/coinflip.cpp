#include<stdio.h>
int main()
{
	int cases,g,i,n,q;
	scanf("%d",&cases);
	while(cases--)
	{
		scanf("%d",&g);
		while(g--)
		{
			scanf("%d%d%d",&i,&n,&q);
			if(i==1)
			{
				if(n%2==0)
			    {
			    	printf("%d\n",n/2);
				}
				else
				{
					if(q==1)
						printf("%d\n",n/2);
					else
					    printf("%d\n",(n/2)+1);
				}
			}
			else
			{
				if(n%2==0)
			    {
			    	printf("%d\n",n/2);
				}
				else
				{
					if(q==1)
						printf("%d\n",(n/2)+1);
					else
					    printf("%d\n",n/2);
				}
			}
		}
	}
	return 0;
}
